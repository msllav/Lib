#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char *s;

	if (!(s = (char*)malloc(sizeof(char) * ft_strlen(s1) + 1)))
		return (NULL);
	return (ft_strcpy(s, s1));
}
